    public void sair(){
        int resp = JOptionPane.showConfirmDialog(
            null,
            MENSAGEM_SAIR,
            TITULO_CONFIRMACAO,
            JOptionPane.YES_NO_OPTION
        );
        if(resp == 0){
            //System.exit(0);
            dispose();
        }
    }
    
    /**
     * Valida se os campos obrigatórios foram preenchidos
     * @return true se os campos estão válidos, false caso contrário
     */
    private boolean validarCampos() {
        String numeroConta = cxNumeroConta.getText().trim();
        String agencia = cxAgencia.getText().trim();

        if (numeroConta.isEmpty() || agencia.isEmpty()) {
            JOptionPane.showMessageDialog(this, MENSAGEM_CAMPOS_VAZIOS, TITULO_ERRO, JOptionPane.ERROR_MESSAGE);
            cxNumeroConta.requestFocus();
            return false;
        }
        return true;
    }
    
    public void limpar() {
        cxNumeroConta.setText("");
        cxAgencia.setText("");
        cxNumeroConta.requestFocus();
    }
    
