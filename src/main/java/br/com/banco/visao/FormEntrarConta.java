    private void btEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntrarActionPerformed
        // Boa Prática 2: Validação de entrada com método separado para melhor legibilidade
        if (!validarCampos()) {
            return;
        }

        String numeroConta = cxNumeroConta.getText().trim();
        String agencia = cxAgencia.getText().trim();

        ContaBancaria conta = GerenciadorDeContas.buscarConta(numeroConta, agencia);

        if (conta != null) {
            abreConta(conta);
        } else {
            JOptionPane.showMessageDialog(this, MENSAGEM_CONTA_INCORRETA, TITULO_ERRO, JOptionPane.ERROR_MESSAGE);
            limpar();
        }
    }//GEN-LAST:event_btEntrarActionPerformed

